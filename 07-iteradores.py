lenguajes = ['Python', 'Java', 'React', 'Kotlin', 'R', 'GO', 'Ruby']

# Iteradores
for lenguaje in lenguajes:
    print(lenguaje)

# Imprimiendo con string
for lenguaje in lenguajes:
    print(f'Estoy aprendiendo {lenguaje}')

# For que escribe numeros
for numero in range(0, 51):
    print(numero)

# For que escribe numeros de dos en dos
for numero in range(0, 51, 2):
    print(numero)